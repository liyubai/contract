package com.cauc.contract;

/**
 * 服务异常基类，抛出此异常表示 业务层出现问题
 * @author fuchaohui 6/26/2012 15:54
 */
public class FinanceServiceException extends FinanceException {

	private static final long serialVersionUID = -5257385546556278151L;
	/**
	 * Construct emtpy exception object.
	 */	
	public FinanceServiceException(){
		super();
	}
	/**
	 * Construct ErsException with message string.
	 * @param s Error message string. 
	 */		
	public FinanceServiceException(String s){
		super(s);
	}
	/**
	 * Construct ErsException, wrapping existing throwable
	 * @param s Error message string.
	 * @param t Existing connection to wrap.
	 */	
	public FinanceServiceException(String s,Throwable t){
		super(s,t);
	}	
	/**
	 * Construct ErsException, wrapping existing throwable.
	 * @param t Existing connection to wrap.
	 */	
	public FinanceServiceException(Throwable t){
		super(t);
	}	
}
