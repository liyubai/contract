package com.cauc.contract.pojos;

public class LoginUserExtends{
	

	private int VId;
	private String VUsername;
	private String VPassword;
	private int VRoleId;
	private String VRealname;
	//角色名
	private String rolename;
	/**
	 * @return the vId
	 */
	public int getVId() {
		return VId;
	}
	/**
	 * @param vId the vId to set
	 */
	public void setVId(int vId) {
		VId = vId;
	}
	/**
	 * @return the vUsername
	 */
	public String getVUsername() {
		return VUsername;
	}
	/**
	 * @param vUsername the vUsername to set
	 */
	public void setVUsername(String vUsername) {
		VUsername = vUsername;
	}
	/**
	 * @return the vPassword
	 */
	public String getVPassword() {
		return VPassword;
	}
	/**
	 * @param vPassword the vPassword to set
	 */
	public void setVPassword(String vPassword) {
		VPassword = vPassword;
	}
	/**
	 * @return the vRoleId
	 */
	public int getVRoleId() {
		return VRoleId;
	}
	/**
	 * @param vRoleId the vRoleId to set
	 */
	public void setVRoleId(int vRoleId) {
		VRoleId = vRoleId;
	}
	/**
	 * @return the rolename
	 */
	public String getRolename() {
		return rolename;
	}
	/**
	 * @param rolename the rolename to set
	 */
	public void setRolename(String rolename) {
		this.rolename = rolename;
	}
	/**
	 * @return the vRealname
	 */
	public String getVRealname() {
		return VRealname;
	}
	/**
	 * @param vRealname the vRealname to set
	 */
	public void setVRealname(String vRealname) {
		VRealname = vRealname;
	}
	

    

}
